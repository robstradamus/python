# Calculadora básica: Crea una calculadora básica que pueda sumar, restar, multiplicar y dividir dos números. 
# Define funciones separadas para cada operación.

def suma(a,b):
    resultado = a + b
    print(resultado)

def restar(a,b):
    resultado = a - b
    print(resultado)
    
def multiplicar(a,b):
    resultado = a * b
    print(resultado)

def division(a,b):
    resultado = a / b
    print(resultado)

suma(2,4)
restar(2,4)
multiplicar(2,4)
division(2,4)